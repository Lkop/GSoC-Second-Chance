package org.lkop.project2ndchance;

public class AwardData {
    
    private int id;
    private int prize;
    
    
    public void setId(int id){
    
        this.id = id;
    }
    
    public int getId(){
    
        return this.id;
    }
    
    public void setPrize(int prize){
    
        this.prize = prize;
    }
    
    public int getPrize(){
    
        return this.prize;
    }
}